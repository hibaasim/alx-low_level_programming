#include <stdio.h>

/**
 * add - adds two ints
 * @a: first num
 * @b: second num
 *
 * Return: result of addition
 */
int add(int a, int b)
{
	return ((a) + (b));
}

/**
 * sub - subtracts two ints
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * mul - multiplies two ints
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * div - divides two ints
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int div(int a, int b)
{
	return ((a) / (b));
}

/**
 * mod - gives remainder of division beween ints
 * @a: first num
 * @b: second num
 *
 * Return: result
 */
int mod(int a, int b)
{
	return ((a) % (b));
}
